    private void loadPartner(String name) {
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        while(model.getRowCount() > 0)  {
            model.removeRow(0);
        }
        map.clear();
        List<Partner> partners = PartnerImpl.findAllPartner(name);
        for(Partner partner : partners) {
            model.addRow(new Object[] {partner.getName()});

            map.put(jTable1.getRowCount()-1, partner.getId());
        }
    }

