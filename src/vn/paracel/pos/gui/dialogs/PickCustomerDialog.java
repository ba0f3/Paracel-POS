    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Integer pid = (Integer) map.get(jTable1.getSelectedRow());
        Partner p = ModelFactory.getPartner(pid);
        RootView.getInstance().getOrderScreen().getTxtCustomerName().setText(p.getName());

        Order order = OrderManager.getInstance().findOrderByTable(Session.getTable());
        order.setPartner(p);
        order.save();

        dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

